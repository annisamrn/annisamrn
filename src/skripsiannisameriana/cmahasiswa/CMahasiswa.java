package skripsiannisameriana.cmahasiswa;

/**
 *
 * @author USER
 */
public class CMahasiswa {
    int idPendaftar;
    String namaPendaftar, asalSekolah, telepon, email, alamat;

    public int getIdPendaftar() {
        return idPendaftar;
    }

    public void setIdPendaftar(int idPendaftar) {
        this.idPendaftar = idPendaftar;
    }

    public String getNamaPendaftar() {
        return namaPendaftar;
    }

    public void setNamaPendaftar(String namaPendaftar) {
        this.namaPendaftar = namaPendaftar;
    }

    public String getAsalSekolah() {
        return asalSekolah;
    }

    public void setAsalSekolah(String asalSekolah) {
        this.asalSekolah = asalSekolah;
    }

    public String getTelepon() {
        return telepon;
    }

    public void setTelepon(String telepon) {
        this.telepon = telepon;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
